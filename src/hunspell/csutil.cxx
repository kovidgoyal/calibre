// primitive isalpha() replacement for tokenization
char * get_casechars(const char * enc) {
    struct cs_info * csconv = get_current_cs(enc);
    char expw[MAXLNLEN];
    char * p =  expw;
    for (int i = 0; i <= 255; i++) {
        if ((csconv[i].cupper != csconv[i].clower)) {
    	    *p = (char) i;
    	    p++;
        }
    }
    *p = '\0';
#ifdef MOZILLA_CLIENT
    delete [] csconv;
#endif
    return mystrdup(expw);
}

